public class TwoNumbers {
    public static void main(String []args){

        int add = 13 + 22;
        System.out.println(add);

        int substract = 13 - 22;
        System.out.println(substract);

        int multiple = 22 * 13;
        System.out.println(multiple);

        double divide = 22/13;
        System.out.println(divide);

        int div = 22/13;
        System.out.println(div);
        System.out.println(divide);
    }
}