public class Swap {
    public static void main(String[] args) {
        // Swap the values of the variables
        int a = 123;
        int b = 526;

        int c = a;
        int a2 = b;
        int b2 = c;

        System.out.println("a = " + a2);
        System.out.println("b = " + b2);
    }
}